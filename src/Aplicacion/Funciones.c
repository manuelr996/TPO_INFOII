void PrenderLedStick( void  )
{
	SetPIN( LED_AZUL , BAJO );
	SetPIN( LED_VERDE , BAJO );
}

void ApagarLedStick( void )
{
	SetPIN( LED_AZUL , ALTO );
	SetPIN( LED_VERDE , ALTO );
}

void FuncLluvia( void )
{
	if( Lluvia == ON )
		PrenderLedStick();
	else
		ApagarLedStick();
}
